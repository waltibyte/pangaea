// Product model
export type Product = {
    id: number,
    title: string
    image_url: string,
    price: (currency: Currency) => {}
    product_options?: ProductOption
}

// Production option model
export type ProductOption = {
    title: string,
    prefix: string,
    suffix: string,
    options: ProductOptionValue
}

// Product option value model
export type ProductOptionValue = {
    id: number,
    value: string
}

